def calc_line_flows(V_mag, V_ang, branch_data):
    """Hitung aliran daya kompleks di semua saluran via model-pi."""

    # tegangan kompleks
    V = V_mag * np.exp(1j * V_ang)

    results = []
    P_loss_total = 0.0

    # ==========================================
    # Loop semua saluran
    # ==========================================
    for branch in branch_data:

        # indeks bus
        fr = int(branch[0]) - 1
        to = int(branch[1]) - 1

        # parameter saluran
        R = branch[2]
        X = branch[3]
        B_half = branch[4]

        # admitansi seri
        y_series = 1.0 / complex(R, X)

        # admitansi shunt
        y_shunt = complex(0, B_half)

        # ==========================================
        # Arus arah fr -> to
        # ==========================================
        I_ij = (
            y_series * (V[fr] - V[to])
            +
            y_shunt * V[fr]
        )

        # ==========================================
        # Arus arah to -> fr
        # ==========================================
        I_ji = (
            y_series * (V[to] - V[fr])
            +
            y_shunt * V[to]
        )

        # ==========================================
        # Daya kompleks
        # ==========================================
        S_ij = V[fr] * np.conj(I_ij)

        S_ji = V[to] * np.conj(I_ji)

        # ==========================================
        # Rugi saluran
        # ==========================================
        S_loss = S_ij + S_ji

        # total rugi aktif
        P_loss_total += S_loss.real

        # simpan hasil
        results.append({

            'saluran': f"{fr+1}-{to+1}",

            'P_ij_pu': S_ij.real,
            'Q_ij_pu': S_ij.imag,

            'P_loss_MW': S_loss.real * 100

        })

    return results, P_loss_total


