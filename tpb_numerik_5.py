def calc_power(V_mag, V_ang, G, B):
    """Hitung P dan Q kalkulasi di semua bus."""

    n = len(V_mag)

    P, Q = np.zeros(n), np.zeros(n)

    for i in range(n):

        for j in range(n):

            t_ij = V_ang[i] - V_ang[j]

            P[i] += (
                V_mag[i] * V_mag[j] *
                (
                    G[i,j] * np.cos(t_ij)
                    +
                    B[i,j] * np.sin(t_ij)
                )
            )

            Q[i] += (
                V_mag[i] * V_mag[j] *
                (
                    G[i,j] * np.sin(t_ij)
                    -
                    B[i,j] * np.cos(t_ij)
                )
            )

    return P, Q


